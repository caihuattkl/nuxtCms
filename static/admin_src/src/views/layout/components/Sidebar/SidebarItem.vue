<template>
  <div class="menu-wrapper">
    <router-link to="/" class="logo">
      <h2>CMS</h2>
      <!-- <img src="~@/assets/images/logo.png" /> -->
    </router-link>
    <template v-for="(item, i) in routes" track-by="$index">
      <div v-if="!item.hidden && item.children" :key="i">
        <router-link @click.native="$router.push({ query: { t: '' } })" v-if="hasOneShowingChildren(item.children, item) && !item.children[0].children && !item.alwaysShow" :to="item.path + '/' + item.children[0].path" :key="item.children[0].name">
          <el-menu-item :index="item.path + '/' + item.children[0].path" :class="{ 'submenu-title-noDropdown': !isNest }">
            <i v-if="item.children[0].meta && item.children[0].meta.icon" :class="item.children[0].meta.icon"></i>
            <span v-if="item.children[0].meta && item.children[0].meta.title">{{ item.children[0].meta.title }}</span>
          </el-menu-item>
        </router-link>
        <el-submenu v-else :index="item.name || item.path" :key="item.name">
          <template slot="title">
            <i v-if="item.meta && item.meta.icon" :class="item.meta.icon"></i>
            <span v-if="item.meta && item.meta.title">{{ item.meta.title }}</span>
          </template>
          <template v-for="(child, index) in item.children" track-by="$index">
            <div v-if="!child.hidden" :key="index">
              <sidebar-item :is-nest="true" class="nest-menu" v-if="child.children && child.children.length > 0" :routes="[child]" :key="child.path"></sidebar-item>
              <router-link v-else :to="item.path + '/' + child.path" :key="child.name">
                <el-menu-item :index="item.path + '/' + child.path">
                  <i v-if="child.meta && child.meta.icon" :class="child.meta.icon"></i>
                  <span v-if="child.meta && child.meta.title">{{ child.meta.title }}</span>
                </el-menu-item>
              </router-link>
            </div>
          </template>
        </el-submenu>
      </div>
    </template>
  </div>
</template>

<script>
export default {
  name: "SidebarItem",
  props: {
    routes: {
      type: Array
    },
    isNest: {
      type: Boolean,
      default: false
    }
  },
  methods: {
    hasOneShowingChildren(children, items) {
      const showingChildren = children.filter(item => {
        return !item.hidden;
      });
      //非首页的二级菜单增加顶级菜单显示
      if (items.children.length === 1 && items.children[0].level !== 1) return false; //二级菜单
      if (showingChildren.length === 1) return true;
      return false;
    },
    reloadRouter({ id }) {
      this.$router.push({ params: Date.now().toString(16) });
    }
  }
};
</script>
